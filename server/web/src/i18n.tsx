const dicts: Record<Lang, Dict> = { zh, en };

interface I18n {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: keyof typeof zh) => string;
}

const I18nContext = createContext<I18n | null>(null);

// detectLang picks zh for zh-* browsers, otherwise English.
function detectLang(): Lang {
  return navigator.language?.toLowerCase().startsWith("zh") ? "zh" : "en";
}

// I18nProvider supplies the language state and translator.
export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(detectLang());
  const value = useMemo<I18n>(
    () => ({ lang, setLang, t: (key) => dicts[lang][key] ?? key }),
    [lang],
  );
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

// useI18n returns the active translator and language controls.
export function useI18n(): I18n {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
